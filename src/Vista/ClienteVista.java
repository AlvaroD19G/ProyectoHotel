    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
     //Extraer datos de los controles
        String identificacion = this.idtxt.getText();
        String nombre = this.nombretxt.getText();
        String telefono = this.teltxt.getText();
        String correo = this.correotxt.getText();
     

        //Agregar miembro al sistema
        this.controlador.insert(new Clientes(identificacion, nombre, telefono, correo));
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarActionPerformed

        String identificacion = this.idtxt.getText();
//Eliminar
        if (!identificacion.isEmpty()) {
            Clientes ClienteEliminar = new Clientes(identificacion);
            controlador.delete( ClienteEliminar);
            this.clear();
}

    }//GEN-LAST:event_btneliminarActionPerformed

    private void buscarbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarbtnActionPerformed
         String identificacion =JOptionPane.showInputDialog(this, "Ingrese la cedula");
        if (!identificacion.isEmpty()){
            this.controlador.read(identificacion);
        }
        
          
    }//GEN-LAST:event_buscarbtnActionPerformed

    private void tlclientesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tlclientesMouseClicked
            if (evt.getClickCount() == 2) { // Verificar si hubo 2 clics (doble clic)
            int row = this.tlclientes.getSelectedRow();
            Object identificacion = tlclientes.getValueAt(row, 0);
            this.controlador.read(identificacion);
        }
    }//GEN-LAST:event_tlclientesMouseClicked

    private void tlclientesKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tlclientesKeyReleased
         if (evt.getKeyCode()==KeyEvent.VK_DELETE){
            int row = this.tlclientes.getSelectedRow();
            if (row>-1){
                Object identificacion = tlclientes.getValueAt(row, 0);
                this.controlador.delete(new Clientes(identificacion.toString()));
            }
        }
    }//GEN-LAST:event_tlclientesKeyReleased

