@Override
    public void clear() {
        this.idtxt.setText("");
        this.nombretxt.setText("");
        this.teltxt.setText("");
        this.correotxt.setText("");
      
    }

@Override
      public void display(Clientes clientes) {
        this.idtxt.setText(clientes.getIdentificacion());
        this.nombretxt.setText(clientes.getNombre());
        this.teltxt.setText(clientes.getTelefono());
        this.correotxt.setText(clientes.getCorreo());
      
    }

@Override
       public void displayAll(Clientes[] regs) {
        DefaultTableModel tableModel=(DefaultTableModel) tlclientes.getModel();
        tableModel.setNumRows(0);
        for(Clientes listclientes:regs){
            Object[] data=listclientes.toArrayObject();
            tableModel.addRow(data);
        }
        this.tlclientes.setModel(tableModel);
    }


